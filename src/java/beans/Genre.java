
package beans;

public class Genre {

    private String name;
    private int id;
    
    public Genre() {
    }
    
    public Genre (String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}

